package scrap;

public class ScrapDataBean {
	private int s_num;
	private String s_name; 
	private int s_info_num;
	private String s_img_path;
	private int s_info_category;
	private String m_id;
	
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public int getS_info_num() {
		return s_info_num;
	}
	public void setS_info_num(int s_info_num) {
		this.s_info_num = s_info_num;
	}
	public String getS_img_path() {
		return s_img_path;
	}
	public void setS_img_path(String s_img_path) {
		this.s_img_path = s_img_path;
	}
	public int getS_info_category() {
		return s_info_category;
	}
	public void setS_info_category(int s_info_category) {
		this.s_info_category = s_info_category;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	
}
